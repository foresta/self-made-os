	DB	0xb4, 0x0e, 0xbb, 0x0f, 0x00, 0xcd, 0x10, 0xeb
	DB	0xee, 0xf4, 0xeb, 0xfd

; Message

	DB		0x0a, 0x0a
	DB		"hello, world"
	DB		0x0a
	DB		0

	TIMES	0x1fe-($-$$) DB 0

